class Dog:          # defining a class

    species = "cenies falinia"

    def __init__(self, name, age):
        self.name = name
        self.age = age 

    def description(self):
        return f"{self.name} is {self.age} years old. "
    
    def speak(self, sound):
        return f"{self.name} says {sound}"

bulldog = Dog("PETER", 4)
